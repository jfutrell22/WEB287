// set up text to print, each item in array is new line
var aText = new Array(
    "Hello and welcome to my portfolio page. My name is John Futrell and I am an aspiring web developer. I have spent the last 10 years in the environmental field but wanted to try something new. Thats when I decided to start taking classes at Wake Tech and found my love for web development. I feel like I've learned so much these last couple years. From the basics of HTML/CSS and JavaScript to Bootstrap, PHP, Python, C#, and ASP.NET Core MVC I've thouroughly enjoyed learning about all of them in the classes I've taken at Wake Tech. I'm excited to continue learning and seeing what the future holds!"
    );
    var iSpeed = 30; // time delay of print out
    var iIndex = 0; // start printing array at this posision
    var iArrLength = aText[0].length; // the length of the text array
    var iScrollAt = 20; // start scrolling up at this many lines
     
    var iTextPos = 0; // initialise text position
    var sContents = ''; // initialise contents variable
    var iRow; // initialise current row
     
    function typewriter()
    {
     sContents =  ' ';
     iRow = Math.max(0, iIndex-iScrollAt);
     var destination = document.getElementById("typedtext");
     
     while ( iRow < iIndex ) {
      sContents += aText[iRow++] + '<br />';
     }
     destination.innerHTML = sContents + aText[iIndex].substring(0, iTextPos) + "_";
     if ( iTextPos++ == iArrLength ) {
      iTextPos = 0;
      iIndex++;
      if ( iIndex != aText.length ) {
       iArrLength = aText[iIndex].length;
       setTimeout("typewriter()", 500);
      }
     } else {
      setTimeout("typewriter()", iSpeed);
     }
    }
    
    
    typewriter();